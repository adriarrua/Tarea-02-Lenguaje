    private void consultarClientePorCodigo(int codigo) {
        //de consultar
        if(codigo != 0){ // si codigo es distinto a cero
            Cliente cliente = clienteController.buscarClientePorCodigo(codigo);
            
            if(cliente != null){
                tfNombre.setText(cliente.getNombres());
                tfApellido.setText(cliente.getApellidos());
                tfDireccion.setText(cliente.getDireccion());
            }else{
                JOptionPane.showMessageDialog(this, "No existe Cliente con este codigo", "Aviso", 1); // 1 = tipo de icono significa question
            }
        }else{
            JOptionPane.showMessageDialog(this, "Debes digitar un Numero...!", "Aviso", 2);
        }
    }
    // para el boton modificar
    private void modificar() {
        Cliente cliente = new Cliente(Integer.parseInt(tfCodigo.getText()),
                tfNombre.getText(), tfApellido.getText(), tfDireccion.getText(),
                tfCelular.getText(), Double.parseDouble(tfCredito.getText()), cbEstado.isSelected());
        
        boolean result = clienteController.modificarCliente(cliente);
        
        if (result){
            JOptionPane.showMessageDialog(this, "Cliente actualizado con exito...!", "Aviso", 1);
            
        }
    }
        private void eliminar(){
            int opcion = JOptionPane.showConfirmDialog(this, "Estas seguro que quiere eliminar?",
                    "Aviso", JOptionPane.YES_NO_OPTION);
        if (opcion == JOptionPane.YES_OPTION){
            clienteController.eliminarCliente(Integer.parseInt(tfCodigo.getText()));
        }
        }
       
        private void habilitarCampos(boolean valor){
            tfNombre.setEnabled(valor);
            tfApellido.setEnabled(valor);
            tfDireccion.setEnabled(valor);
            tfCredito.setEnabled(valor);
            tfCelular.setEnabled(valor);
            cbEstado.setEnabled(valor);
        }

    private boolean validarCampos() {
        boolean valido = false;
            if(tfNombre.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "El campo Nombres es obligatorio", "Aviso", 1);
                tfNombre.requestFocus();
            }else if(tfApellido.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "El campo Apellidos es obligatorio", "Aviso", 1);
                tfApellido.requestFocus();
            }else if(tfDireccion.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "El campo Direccion es obligatorio", "Aviso", 1);
                tfDireccion.requestFocus();
            }else if(tfCredito.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "El campo Credito es obligatorio", "Aviso", 1);
                tfCredito.requestFocus();
            }else if(tfCelular.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "El campo Celular es obligatorio", "Aviso", 1);
                tfCredito.requestFocus();    
             }else if(cbEstado.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "El estado es obligatorio", "Aviso", 1);
                tfCredito.requestFocus();     
                
            }else{
                valido = true;
            }   
           return valido; 
         
        
    
    }
      
