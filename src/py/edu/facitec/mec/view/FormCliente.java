    private void registrarCliente() {
        if (validarCampos()){
        Cliente cliente = new Cliente(); // creamos un objeto cliente
        cliente.setNombres(tfNombre.getText());
        cliente.setApellidos(tfApellido.getText());
        cliente.setDireccion(tfDireccion.getText());
        cliente.setCredito(Double.valueOf("10000"));
        cliente.setCelular(tfCelular.getText());
        cliente.setEstado(cbEstado.isSelected());
        
        clienteController.guardarCliente(cliente);
            limpiarCampos();
            
            verMensaje("El cliente se ha registrado con exito!", 1);
    }
    }
    
